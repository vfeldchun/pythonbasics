print('Программа использования класса Road!')

r_1 = Road(5000, 20) # Создаем обьект класса Road с указанием ширины дороги 20 метров и длинны 5000 м
r_1.get_class_info() # Выводим краткую справку по работе класса Road на экран

print(f'Вес асфальтобетона для дороги шириной 20м,'
      f' дленной 5000м и шириной 5 см. составит: \n{r_1.road_weigt_calc()} тонн!') # Выводим вес дорожного покрытия

