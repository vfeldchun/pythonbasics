# Запрашиваем у пользователя данные и выводим на экран результат возведения в степень
# полученый двумя способами

print('Возведение действительного положительного числа в отрицательную степень!\n')
num = float(input('Введите действительное положительное число: '))
exponent = int(input('Введите целое отрицательное число: '))

if num < 0: num = num * -1 # если пользователь ввел действительное отрицательное число превращаем его в положительное
if exponent > 0: exponent = exponent * -1 # если пользователь целое положительное число то превращаем его в отрицательное

print(f'Возведение в степень {exponent} числа {num} через операцию **')
print(f'Оезультат: {my_func(num, exponent)}\n')
print(f'Возведение в степень {exponent} числа {num} через цикл')
print(f'Результат: {my_func(num, exponent, 2)}\n')

